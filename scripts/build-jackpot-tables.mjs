// =============================================================================
// Transmarvel Overhaul — Warpath+ auto-pruner (save -> Reloaded config)
// =============================================================================
//
// WHAT THIS DOES
//   Reads your save (read-only), works out which (Warpath+ sigil x 2nd trait)
//   combos you already own, and updates the mod's Reloaded-II config:
//   a Warpath+ whose every *rollable* combo you own gets UNTICKED from the
//   Transmarvel pool (a pull could give you nothing new); one with combos
//   still missing gets ticked back on. The mod's C# component rebuilds the
//   actual gacha tables from that config at the next game launch.
//
//   "Rollable" respects your trait checkboxes AND vanilla legality: only
//   ticked traits that can naturally roll on Transmarvel pulls count
//   (trait-catalog.json `legalOnTransmarvel`). The script only manages the
//   28 Warpath+ ticks — your chase-V+ ticks and everything else are yours.
//
//   History: v1..v2.0-dev5 of this script BUILT gacha .tbl files directly;
//   since 2.0-dev6 the pool is generated by the mod dll from the config,
//   so this script's output is the config itself. Save format: docs/21.
//
// HOW TO RUN
//   node scripts/build-jackpot-tables.mjs        (from the repo root)
//   ...then RESTART THE GAME (tables are generated once, at launch).
// =============================================================================

import { readFileSync, writeFileSync, existsSync, readdirSync, statSync, mkdirSync } from 'node:fs';

const SAVE_DIR = `${process.env.LOCALAPPDATA}\\GBFR\\Saved\\SaveGames`;
const USER_CFG = 'C:\\Reloaded-II\\User\\Mods\\gbfr.transmarvel.overhaul\\Config.json';
const SIGILS = JSON.parse(readFileSync('mods/transmarvel-overhaul/sigil-catalog.json', 'utf8'));
const TRAITS = JSON.parse(readFileSync('mods/transmarvel-overhaul/trait-catalog.json', 'utf8'));

let userCfg = {};
if (existsSync(USER_CFG)) {
  try { userCfg = JSON.parse(readFileSync(USER_CFG, 'utf8')); } catch { /* defaults */ }
}
const ticked = (entry) => userCfg[entry.prop] ?? entry.default;

// combos that can actually roll on a Transmarvel pull
const CURATED = Object.fromEntries(
  TRAITS.filter(t => ticked(t) && t.legalOnTransmarvel).map(t => [t.id, t.name]));
const N_CURATED = Object.keys(CURATED).length;
if (N_CURATED === 0) {
  console.error('No ticked trait can roll on Transmarvel pulls — nothing to prune against. Tick some traits first.');
  process.exit(1);
}
const WARPATH = SIGILS.filter(s => s.bucket === 'Warpath+');

// --- GBFR custom XXHash32 (docs/15) ---
function xxh32(str) {
  const data = Buffer.from(str, 'ascii');
  const P1 = 0x9e3779b1, P2 = 0x85ebca77, P3 = 0xc2b2ae3d, P4 = 0x27d4eb2f, P5 = 0x165667b1;
  const rotl = (x, r) => ((x << r) | (x >>> (32 - r))) >>> 0;
  const mul = (a, b) => Math.imul(a, b) >>> 0;
  const round = (acc, input) => mul(rotl((acc + mul(input, P2)) >>> 0, 13), P1);
  const len = data.length;
  let h = 0x178A54A4, i = 0;
  if (len >= 16) {
    let v1 = 0x2557311B, v2 = 0x871FB76A, v3 = 0x0133ECF3, v4 = 0x62FC7342;
    do {
      v1 = round(v1, data.readUInt32LE(i)); v2 = round(v2, data.readUInt32LE(i + 4));
      v3 = round(v3, data.readUInt32LE(i + 8)); v4 = round(v4, data.readUInt32LE(i + 12));
      i += 16;
    } while (len - i > 16);
    h = (rotl(v1, 1) + rotl(v2, 7) + rotl(v3, 12) + rotl(v4, 18)) >>> 0;
  }
  h = (h + len) >>> 0;
  for (; len - i >= 4; i += 4) h = mul(rotl((h + mul(data.readUInt32LE(i), P3)) >>> 0, 17), P4);
  for (; i < len; i++) h = mul(rotl((h + mul(data[i], P5)) >>> 0, 11), P1);
  h = mul(h ^ (h >>> 15), P2);
  h = mul(h ^ (h >>> 13), P3);
  return (h ^ (h >>> 16)) >>> 0;
}

// --- read owned (Warpath+ x secondary) combos from the newest save ---
function readOwnedCombos() {
  const die = (msg) => { console.error(`SAVE READ: ${msg}`); process.exit(1); };
  if (!existsSync(SAVE_DIR)) die(`save folder not found: ${SAVE_DIR}`);
  const slots = readdirSync(SAVE_DIR).filter(f => /^SaveData\d+\.dat$/i.test(f));
  if (!slots.length) die(`no SaveData<N>.dat in ${SAVE_DIR}`);
  const save = slots.map(f => `${SAVE_DIR}\\${f}`).sort((a, b) => statSync(b).mtimeMs - statSync(a).mtimeMs)[0];
  const buf = readFileSync(save);
  const EMPTY = 0x887ae0b0;

  const geenRev = new Map(), skillRev = new Map();
  for (let a = 0; a <= 999; a++) {
    for (let b = 0; b <= 99; b++)
      geenRev.set(xxh32(`GEEN_${String(a).padStart(3, '0')}_${String(b).padStart(2, '0')}`), { n: a });
    for (let b = 0; b <= 10; b++)
      skillRev.set(xxh32(`SKILL_${String(a).padStart(3, '0')}_${String(b).padStart(2, '0')}`), { n: a, id: `SKILL_${String(a).padStart(3, '0')}_${String(b).padStart(2, '0')}` });
  }

  const locate = (rev, what) => {
    for (let off = 0; off + 24 <= buf.length; off += 4) {
      if (buf.readUInt32LE(off + 8) !== 4 || buf.readUInt32LE(off + 12) !== 1) continue;
      if (!rev.has(buf.readUInt32LE(off + 16))) continue;
      const type = buf.readUInt32LE(off);
      if (type === 0 || type > 0xffff) continue;
      let s = off, e = off;
      while (s - 24 >= 0 && buf.readUInt32LE(s - 24) === type) s -= 24;
      while (e + 48 <= buf.length && buf.readUInt32LE(e + 24) === type) e += 24;
      const count = (e - s) / 24 + 1;
      if (count >= 1000) {
        const seen = new Set(); let dup = 0;
        for (let i = 0; i < count; i++) {
          const h = buf.readUInt32LE(s + 24 * i + 16);
          if (h !== EMPTY) { if (seen.has(h)) dup++; else seen.add(h); }
        }
        if (dup > 10) return { start: s, count };
      }
      off = e;
    }
    die(`could not find the ${what} array — save layout changed?`);
  };
  const sig = locate(geenRev, 'sigil inventory');
  const skl = locate(skillRev, 'trait pool');
  const recAt = (a, i) => ({ seq: buf.readUInt32LE(a.start + 24 * i + 4), hash: buf.readUInt32LE(a.start + 24 * i + 16) });

  const pairs = new Map();
  for (let i = 0; i < skl.count; i++) {
    const r = recAt(skl, i);
    const N = Math.floor(r.seq / 100);
    if (!pairs.has(N)) pairs.set(N, {});
    pairs.get(N)[r.seq % 100] = r.hash;
  }
  const filled = [];
  for (let j = 0; j < sig.count; j++) {
    const g = geenRev.get(recAt(sig, j).hash);
    if (g) filled.push({ j, n: g.n, hash: recAt(sig, j).hash });
  }
  if (!filled.length) die('sigil inventory is empty?');
  // align via the innate-trait redundancy (SKILL number == GEEN number)
  const votes = new Map();
  for (const { j, n } of filled)
    for (const [N, p] of pairs) {
      const s0 = skillRev.get(p[0]);
      if (s0 && s0.n === n) votes.set(N + j, (votes.get(N + j) || 0) + 1);
    }
  const C = [...votes.entries()].sort((a, b) => b[1] - a[1])[0][0];
  let match = 0;
  for (const { j, n } of filled) {
    const s0 = skillRev.get((pairs.get(C - j) || {})[0]);
    if (s0 && s0.n === n) match++;
  }
  if (match / filled.length < 0.99)
    die(`alignment cross-check failed (${match}/${filled.length}) — save layout changed?`);

  const warpathByHash = new Map(WARPATH.map(w => [parseInt(w.hash, 16), w]));
  const combos = new Map(WARPATH.map(w => [w.prop, new Set()]));
  for (const { j, hash } of filled) {
    const w = warpathByHash.get(hash);
    if (!w) continue;
    const sec = skillRev.get((pairs.get(C - j) || {})[1]);
    if (sec && CURATED[sec.id]) combos.get(w.prop).add(sec.id);
  }
  console.log(`Read ${save}: ${filled.length} sigils, alignment ${match}/${filled.length}.`);
  return combos;
}

// --- update the Reloaded config: untick complete Warpath+, tick back incomplete ---
const combos = readOwnedCombos();
console.log(`Rollable combo target: ${N_CURATED} traits per Warpath+ (${Object.values(CURATED).join(', ')})\n`);
let pruned = 0, restored = 0;
for (const w of WARPATH) {
  const cov = combos.get(w.prop).size;
  const complete = cov === N_CURATED;
  const was = ticked(w);
  if (complete && was) { userCfg[w.prop] = false; pruned++; }
  if (!complete && !was) { userCfg[w.prop] = true; restored++; }
  const state = complete ? 'COMPLETE -> out of pool' : `${cov}/${N_CURATED}`;
  console.log(`  ${w.name}: ${state}${!complete && !was ? ' (re-ticked)' : ''}`);
}
mkdirSync(USER_CFG.slice(0, USER_CFG.lastIndexOf('\\')), { recursive: true });
writeFileSync(USER_CFG, JSON.stringify(userCfg, null, 2) + '\n');
JSON.parse(readFileSync(USER_CFG, 'utf8'));
console.log(`\nConfig updated (${USER_CFG}): ${pruned} pruned, ${restored} re-ticked.`);
console.log('RESTART THE GAME to apply (the mod rebuilds the pool from the config at launch).');
